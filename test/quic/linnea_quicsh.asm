; linnea_quicsh.asm — test-only: build the QUIC ServerHello with a fixed
; server key share and random, and write the raw message to stdout so aioquic's
; TLS parser (sh_parse.py) can confirm it interoperates.

%include "linnea_syscall.inc"

global _start
extern linnea_quic_build_sh

section .rodata
server_pub:
    db 0xa0, 0xa1, 0xa2, 0xa3, 0xa4, 0xa5, 0xa6, 0xa7, 0xa8, 0xa9, 0xaa, 0xab, 0xac, 0xad, 0xae, 0xaf
    db 0xb0, 0xb1, 0xb2, 0xb3, 0xb4, 0xb5, 0xb6, 0xb7, 0xb8, 0xb9, 0xba, 0xbb, 0xbc, 0xbd, 0xbe, 0xbf
srand:
    db 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f
    db 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f

section .bss
sh_buf: resb 256

section .text
_start:
    lea rdi, [sh_buf]
    lea rsi, [server_pub]
    lea rdx, [srand]
    call linnea_quic_build_sh        ; rax = length
    mov rdx, rax
    mov edi, 1
    lea rsi, [sh_buf]
    mov eax, 1                        ; write(1, sh_buf, len)
    syscall
    xor edi, edi
    mov eax, LINNEA_SYS_EXIT
    syscall
